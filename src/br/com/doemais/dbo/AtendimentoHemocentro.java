package br.com.doemais.dbo;

public class AtendimentoHemocentro {
	private Integer hemocentroId;
	private int dia;
	private int diaFinal;
	private String horaFinal;
	private String horaInicio;
	private String periodoFinal;
	private String periodoInicio;
	private int quantidade;
	private int tempo;
	
	public Integer getHemocentroId() {
		return hemocentroId;
	}
	public void setHemocentroId(Integer hemocentroId) {
		this.hemocentroId = hemocentroId;
	}
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	public int getDiaFinal() {
		return diaFinal;
	}
	public void setDiaFinal(int diaFinal) {
		this.diaFinal = diaFinal;
	}
	public String getHoraFinal() {
		return horaFinal;
	}
	public void setHoraFinal(String horaFinal) {
		this.horaFinal = horaFinal;
	}
	public String getHoraInicio() {
		return horaInicio;
	}
	public void setHoraInicio(String horaInicio) {
		this.horaInicio = horaInicio;
	}
	public String getPeriodoFinal() {
		return periodoFinal;
	}
	public void setPeriodoFinal(String periodoFinal) {
		this.periodoFinal = periodoFinal;
	}
	public String getPeriodoInicio() {
		return periodoInicio;
	}
	public void setPeriodoInicio(String periodoInicio) {
		this.periodoInicio = periodoInicio;
	}
	public int getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	public int getTempo() {
		return tempo;
	}
	public void setTempo(int tempo) {
		this.tempo = tempo;
	}
	
	
	
}
